CREATE TABLE IF NOT EXISTS BIPU010 (
    COLLECTOR_NAME VARCHAR(40) COMMENT '수집기 이름',
    COLLECTOR_ADDR VARCHAR(15) COMMENT '수집기 IP 주소',
    COLLECTOR_OFFSET_STRING VARCHAR(23) COMMENT '수집기 오프셋 문자열',
    SNO BIGINT COMMENT '일련번호',
    COMCD VARCHAR(5) COMMENT '회사코드',
    SUCD VARCHAR(5) COMMENT '공급사코드',
    BRCD VARCHAR(5) COMMENT '지점코드',
    YSCD VARCHAR(10) COMMENT '여성코드',
    YEAR VARCHAR(10) COMMENT '년도',
    SEASON VARCHAR(10) COMMENT '시즌',
    GUBUN CHAR(1) COMMENT '구분',
    SOJAE VARCHAR(10) COMMENT '소재',
    SOJAENM VARCHAR(30) COMMENT '소재명',
    YSMODEL BIGINT COMMENT '여성모델',
    YSQTY BIGINT COMMENT '여성수량',
