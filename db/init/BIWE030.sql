CREATE TABLE IF NOT EXISTS BIWE030 (
    COLLECTOR_NAME VARCHAR(40) COMMENT '수집기 이름',
    COLLECTOR_ADDR VARCHAR(15) COMMENT '수집기 IP 주소',
    COLLECTOR_OFFSET_STRING VARCHAR(23) COMMENT '수집기 오프셋 문자열',
    SNO BIGINT COMMENT '일련번호',
    COMCD VARCHAR(5) COMMENT '회사코드',
    SUCD VARCHAR(5) COMMENT '공급사코드',
    BRCD VARCHAR(5) COMMENT '지점코드',
    COLCD VARCHAR(5) COMMENT '색상코드',
    STYCD VARCHAR(20) COMMENT '스타일코드',
    MAINSTYCD VARCHAR(20) COMMENT '메인스타일코드',
    ITEM VARCHAR(10) COMMENT '품목',
    INOUTDT VARCHAR(8) COMMENT '입출고일자',
    SOJAENM VARCHAR(100) COMMENT '소재명',
    CUSTNM VARCHAR(100) COMMENT '고객명',
    VDCD VARCHAR(20) COMMENT '벤더코드',
    YSCD VARCHAR(10) COMMENT '연도코드',
    DIMAGEPATH VARCHAR(200) COMMENT '이미지경로',
    VDSNM VARCHAR(100) COMMENT '벤더명',
    INDT VARCHAR(8) COMMENT '입고일자',
    OUTDT VARCHAR(8) COMMENT '출고일자',
    INQTY BIGINT COMMENT '입고수량',
    INAMT BIGINT COMMENT '입고금액',
    SQTY BIGINT COMMENT '판매수량',
    SILAMT BIGINT COMMENT '실매출액',
    TAGPRI BIGINT COMMENT '단가',
    ONLINESQTY BIGINT COMMENT '온라인판매수량',
    ONLINEAMT BIGINT COMMENT '온라인금액',
    CHINASQTY BIGINT COMMENT '중국판매수량',
    CHINAAMT BIGINT COMMENT '중국금액',
    PQTY BIGINT COMMENT '반품수량',
    CREATEDATE DATETIME COMMENT '생성일시',
    REMARK VARCHAR(1000) COMMENT '비고',
    PRIMARY KEY (SNO),
    INDEX IDX_BIWE030_COMCD (COMCD),
    INDEX IDX_BIWE030_SUCD (SUCD),
    INDEX IDX_BIWE030_BRCD (BRCD),
    INDEX IDX_BIWE030_COLCD (COLCD),
    INDEX IDX_BIWE030_STYCD (STYCD),
    INDEX IDX_BIWE030_MAINSTYCD (MAINSTYCD),
    INDEX IDX_BIWE030_INOUTDT (INOUTDT)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='로그 테이블'; 