CREATE TABLE IF NOT EXISTS BION070 (
    COLLECTOR_NAME VARCHAR(40) COMMENT '수집기 이름',
    COLLECTOR_ADDR VARCHAR(15) COMMENT '수집기 IP 주소',
    COLLECTOR_OFFSET_STRING VARCHAR(23) COMMENT '수집기 오프셋 문자열',
    SNO BIGINT COMMENT '일련번호',
    SUCD VARCHAR(5) COMMENT '공급사코드',
    BRCD VARCHAR(5) COMMENT '지점코드',
    ORDER_DT VARCHAR(8) COMMENT '주문일자',
    BRAND_MNG_IDX VARCHAR(10) COMMENT '브랜드관리인덱스',
    FORMAL_DIV VARCHAR(2) COMMENT '정형구분',
    ORDER_QTY BIGINT COMMENT '주문수량',
    ORDER_AMT BIGINT COMMENT '주문금액',
    CANCEL_QTY BIGINT COMMENT '취소수량',
    CANCEL_AMT BIGINT COMMENT '취소금액',
    RETURN_QTY BIGINT COMMENT '반품수량',
    RETURN_AMT BIGINT COMMENT '반품금액',
    SELL_QTY BIGINT COMMENT '판매수량',
    SELL_AMT BIGINT COMMENT '판매금액',
    COUPON_DISCNT_AMT BIGINT COMMENT '쿠폰할인금액',
    POINT_DISCNT_AMT BIGINT COMMENT '포인트할인금액',
    SHIP_AMT BIGINT COMMENT '배송금액',
    PAY_AMT BIGINT COMMENT '결제금액',
    CREATEDATE DATETIME COMMENT '생성일시',
    PRIMARY KEY (SNO)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='로그 테이블'; 